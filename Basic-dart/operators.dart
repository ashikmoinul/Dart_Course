main(){
  int a = 12+13;
  int b = 13-6;
  int c = 12*13;
  double d = 17/2;
  int e = 31 % 2;

  print(a);
  print(b);
  print(c);
  print(d);
  print(e);

  print(a++); //Postfix increment
  print(a);

  print(a--); //Postfix increment
  print(a);

  print(--a); // Postfix decrement
  print(++a); // Prefix decrement

  a+= 10; // a = a + 10
  a-= 10; // a = a - 10
  a*=10; // a = a * 10
  a%= 10; // // a = a / 10





}